package General;
public class bit1{
    public static void main(String[] args) {
        int a=5,b=6;
        int and_opr=a&b;
        int or_opr=a|b;
        int xor_opr=a^b;
        System.out.println("And operation "+and_opr);
        System.out.println("OR operation "+or_opr);
        System.out.println("XOR operation "+xor_opr);
        System.out.println(~5);

    }
}